CREATE DATABASE clinica_candido_torres_database;

USE clinica_candido_torres_database;

CREATE TABLE USUARIO(
	IDUSUARIO INT(5) PRIMARY KEY AUTO_INCREMENT,
	NOME VARCHAR(100) NOT NULL,
	LOGIN VARCHAR(50) NOT NULL UNIQUE,
	SENHA VARCHAR(50) NOT NULL,
	TIPOUSUARIO VARCHAR(50) NOT NULL
);

CREATE TABLE PACIENTE(
	IDPACIENTE INT PRIMARY KEY AUTO_INCREMENT,
        NOME VARCHAR(100) NOT NULL,
	NUMEROPRONTUARIO VARCHAR(100) NOT NULL,
	SEXO VARCHAR(50) NOT NULL,
	DATANASC VARCHAR(50) NOT NULL,
	CPF VARCHAR(50) NOT NULL,
        RG VARCHAR(50) NOT NULL,
        EMAIL VARCHAR(50) NOT NULL,
        PROFISSAO VARCHAR(50) NOT NULL,
        TIPOATENDIMENTO VARCHAR(50) NOT NULL,
        ACOMPANHANTE VARCHAR(50) NOT NULL,
        ESTRANGEIRO VARCHAR(50) NOT NULL,
        TELEFONE VARCHAR(50),
        CELULAR VARCHAR(50) NOT NULL,
        INDICACAO VARCHAR(50) NOT NULL,
        ESTADOCIVIL VARCHAR(50) NOT NULL,
        ENDERECO VARCHAR(50) NOT NULL,
        BAIRRO VARCHAR(50) NOT NULL,
        NUMERO VARCHAR(50) NOT NULL,
        CIDADE VARCHAR(50) NOT NULL,
        ESTADO VARCHAR(50) NOT NULL,
        COMPLEMENTO VARCHAR(50) NOT NULL,
        CEP VARCHAR(50) NOT NULL
);